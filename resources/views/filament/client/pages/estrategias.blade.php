    <h2 class="mt-10 text-center text-4xl uppercase my-2 font-bold">
      ¿Cómo crear una estrategia de trading?
    </h2>

    <p class="text-lg font-medium my-5 text-justify">
      Lo que debes definir es en qué tipo de mercado (forex, valores, derivados,
      etc) y con qué tipo de activo financiero vas a operar (futuros, divisas,
      acciones, etc.), finalmente qué tipo de análisis te permitirá comprender
      mejor el comportamiento de su precio. Por ejemplo, si quieres operar el
      mercado de futuros de índices bursátiles usando Frspot, necesitarías
      desarrollar conocimientos de análisis técnico, es decir, saber interpretar
      gráficas con el fin de comprender la volatilidad del precio de los
      contratos de futuros sobre índices bursátiles durante el día, esto con el
      fin de encontrar momentos para comprar y vender. Por otro lado, ten en
      cuenta la disponibilidad de tiempo que tengas para operar y el capital con
      el que cuentes. Esto te será de gran ayuda para saber qué tipo de
      estrategia funciona mejor para ti.
    </p>


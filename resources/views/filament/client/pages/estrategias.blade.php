            Swing trading
          </h3>
          <p class="text-lg font-medium my-5 text-justify">
            La estrategia de swing trading se aplica gracias a la oscilación del
            precio de un activo financiero; de esta forma, se busca comprarlo
            esperando que vaya a subir su precio en el futuro, asimismo, se
            busca venderlo cuando alcance su punto más alto justo antes de que
            baje de precio. Utilizar una estrategia de este tipo requiere de
            conocimientos de análisis técnico, de esta forma, la comprensión del
            comportamiento del precio es más sencilla y se puede predecir de
            mejor manera la probabilidad de que los activos financieros alcancen
            ciertos precios para venderlos o comprarlos.
          </p>

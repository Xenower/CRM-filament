    <h2 class="mt-10 text-center text-4xl uppercase my-2 font-bold">
      Trading a largo plazo
    </h2>
    <p class="text-lg font-medium my-5 text-justify">
      Hacer trading a largo plazo implica comprar un activo y mantenerlo por un
      periodo de tiempo de un año o más. El objetivo es adquirir un activo a un
      bajo precio para posteriormente esperar a que su precio incremente con el
      paso del tiempo. De esta manera, obtienes rentabilidad al venderlo a un
      precio más costoso en el futuro. Para realizarlo hay que tener en cuenta
      que la inversión inicial se mantendrá sin poder utilizarse hasta dentro de
      un largo periodo de tiempo; por lo tanto, no tendrás liquidez o alguna
      facilidad de acceso a ese dinero. Por esta razón, este tipo de trading
      requiere de paciencia.
    </p>

    <p class="text-lg font-medium my-5 text-justify">
      El análisis fundamental es una excelente herramienta para encontrar
      oportunidades que demuestren perspectivas de crecimiento positivo a
      futuro. Este consiste en analizar los estados financieros de una compañía
      que cotiza en la bolsa de valores o la perspectiva de la industria de
      algún commodity. Es decir, información relacionada a aspectos
      macroeconómicos que sustentan el posible crecimiento de un activo
      financiero.
    </p>


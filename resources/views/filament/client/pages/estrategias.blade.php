    <h2 class="mt-10 text-center text-4xl uppercase my-2 font-bold">
      Trading a corto plazo
    </h2>

    <p class="text-lg font-medium my-5 text-justify">
      El trading a corto plazo implica la compraventa de activos aprovechando
      las fluctuaciones en su precio, en el cual las entradas y salidas de las
      operaciones duran desde unos cuantos segundos hasta un par de semanas.
      Este tipo de trading aprovecha a su favor la volatilidad de los activos;
      sin embargo, ante estos cambios constantes en los precios siempre es
      recomendable conocer todo lo posible en relación a gestión de riesgo, con
      la finalidad de minimizar pérdidas y maximizar ganancias. Otro aspecto
      importante es el uso del análisis técnico para detectar oportunidades de
      compra y venta; debido a que se opera el mercado con intervalos cortos de
      tiempo, este te permitirá entender de mejor manera el comportamiento de
      los activos para encontrar tendencias a tiempo. Para crear más certeza en
      tus decisiones, utiliza también indicadores técnicos, los cuales son
      herramientas para conocer más sobre el volumen, volatilidad y el
      movimiento en el precio de un activo. Estas son cinco estrategias de
      trading a corto plazo que debes conocer:
    </p>

    <div class="flex flex-col gap-6 text-center my-6">
      <h3 class="uppercase text-xl font-semibold">Scalping trading</h3>
      <p class="text-lg font-medium my-5 text-justify">
        La estrategia de scalping trading se enfoca en aprovechar los cortos
        movimientos del precio de los activos con el fin de obtener niveles
        bajos de rentabilidad; la intención es la de acumular rentabilidades
        pequeñas. Para llevarla a cabo deberás cerrar y abrir posiciones en
        cuestión de segundos hasta un par de minutos. Ten en cuenta que se
        considera un estilo para traders avanzados, pues requiere de mucha
        concentración y un pensamiento ágil para tomar decisiones.
      </p>

      <h3 class="uppercase text-xl font-semibold">Day Trading</h3>
      <p class="text-lg font-medium my-5 text-justify">
        A la estrategia de Day Trading también se le conoce como trading
        intradía. Consiste en realizar la operación durante un mismo día,
        aprovechando la fluctuación que los precios de los activos tienen.
        <br />
        Gracias a la volatilidad es que esta estrategia resulta efectiva. El
        movimiento constante en los precios durante el día permite que se tomen
        posiciones rápidas para obtener rentabilidad. Este tipo de estrategia es
        una gran opción para quienes buscan desarrollar una carrera profesional
        dentro del mundo del trading.
      </p>


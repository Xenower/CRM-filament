      <h3 class="uppercase text-xl font-semibold">Trading de tendencia</h3>
      <p class="text-lg font-medium text-justify">
        Esta estrategia se centra en el análisis técnico de las tendencias de
        los precios de los activos financieros. Gracias al uso de las gráficas
        se puede acceder a información histórica del precio de una acción, de
        esta manera es posible identificar el tipo de comportamiento de un
        activo y predecir cómo lo hará en el futuro. Es necesario tener gran
        conocimiento en las tendencias de los precios de los activos, de otra
        forma, el cambio puede presentarse de forma repentina y ocasionar
        grandes pérdidas.
      </p>

      <h3 class="uppercase text-xl font-semibold">Trading final del día</h3>
      <p class="text-lg font-medium my-5 text-justify">
        Este tipo de estrategia implica que la operación del mercado solo se
        realizará en sus últimos momentos de actividad, es decir, antes de que
        cierre. De esta forma se busca tener más certeza sobre qué acción tomar
        pues se espera que los cambios en el precio ya no sean tan drásticos.
      </p>

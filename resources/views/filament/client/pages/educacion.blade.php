    <h2 class='mt-10 text-center text-4xl uppercase my-2 font-bold'>Copy Trading</h2>

    <p class='text-lg font-medium my-5 text-justify'>Con el Copy Trading la cuenta del proveedor de señales de trading y la cuenta del trader están vinculadas. Cuando el proveedor de señales abre una operación en su cuenta, ésta se abre automáticamente en la cuenta del trader. Esta es una solución intermedia, ya que el trading se automatiza y se comparte el control de la cuenta. El proveedor abre posiciones de forma remota en la cuenta del cliente sin tener más control sobre él, y el trader también retiene el control al poder intervenir libremente si lo desea en la operación. Son señales de trading de pago, ya que conlleva una suscripción.</p>

    <h2 class='mt-10 text-center text-4xl uppercase my-2 font-bold'>Señales de trading manuales</h2>

    <p class='text-lg font-medium my-5 text-justify'>Las señales de Forex manuales son generadas por una persona. Para ello, el trader tiene que sentarse delante del ordenador durante una gran cantidad de tiempo, analizar en profundidad, buscando señales para tomar una decisión final sobre si comprar o vender en corto un activo determinado. Las decisiones tomadas por un trader en esa situación están basadas en su interpretación y puede requerir bastante tiempo.</p>


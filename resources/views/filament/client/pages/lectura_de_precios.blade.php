    <h3 class="mt-10 text-center text-4xl uppercase my-2 font-bold">Cómo leer un gráfico de trading de Barras OHLC</h3>

    <p class="text-lg font-medium  my-5 text-justifytext-justify">Un gráfico de barras OHLC muestra una barra para cada período de tiempo que tengas seleccionado. Entonces, si analizas un gráfico de trading diario, cada barra vertical representa el valor de un día de negociación.  </p>

    <div class="">
        <img src="{{asset('client-imgs/grafc6.png')}}" alt="illustracion 6" class='mx-auto my-3' />
    </div>

    <ul class=''>
        <p class='text-xl font font-semibold'>Cómo interpretar un gráfico Forex de barras </p>
        <li class='font-medium text-lg ml-5 my-2'>⇨ El guión de la izquierda representa el precio de apertura</li>
        <li class='font-medium text-lg ml-5 my-2'>⇨ El guión de la derecha representa el precio de cierre</li>
        <li class='font-medium text-lg ml-5 my-2'>⇨ El máximo de la barra es el precio más alto alcanzado durante el período de tiempo seleccionado</li>
        <li class='font-medium text-lg ml-5 my-2'>⇨ El mínimo de la barra es el precio más bajo alcanzado durante el período de tiempo seleccionado.</li>
    </ul>

    <p class='font-medium text-lg my-5 text-justify'>➤ Las barras azules se conocen como barras de comprador o alcistas, ya que el precio de cierre está por encima del precio de apertura.</p>

    <p class="font-medium text-lg my-5 text-justify">➤ Las barras rojas se conocen como barras de vendedor o bajistas, ya que el precio de cierre está por debajo del precio de apertura.</p>

    <p class='font-medium text-lg my-5 text-justify'>En cualquier caso, los gráficos de barras ayudan a los traders a identificar quién tiene el control del mercado: compradores o vendedores. Además, estas barras forman la base del siguiente tipo de gráfico de trading llamado gráfico de velas, que es el más popular.</p>

